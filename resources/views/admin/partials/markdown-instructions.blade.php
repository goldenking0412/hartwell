				<div class="row markdown-instructions">
					<div class="col-md-12">
						<p>
							Markdown is a text-to-HTML conversion tool for web writers. It allows you to write using an easy-to-read, easy-to-write plain text format, then convert it to structurally valid HTML.
						</p>
						<p>
							Remember - the editor panel allows you to place markdown syntax on the fly. This menu should serve as a short reference guide. You can find more detailed info about markdown <a href="http://daringfireball.net/projects/markdown/" target="_blank">here</a>.
						</p>
						<h4>
							Here are some quick examples to get you started
						</h4>
						<hr />
						<h5>Bold</h5>
						<p>This is **bold text**</p>
						<div class="well">
							<span class="result">result:</span>
							<p> This is <strong>bold text</strong></p>
						</div>
						<h5>Italicize</h5>
						<p>This is *italic text*</p>
						<div class="well">
							<span class="result">result:</span>
							<p> This is <em>italic text</em></p>
						</div>
						<h5>Headers</h5>
						<p>
							#### This is a level 4 header<br />
							### This is a level 3 header
						</p>
						<div class="well">
							<span class="result">result:</span>
							<h4>This is a level 4 header</h4>
							<h3>This is a level 3 header</h3>
						</div>
						<h5>Links</h5>
						<p>Have you ever heard of [Google?](http://www.google.com/)</p>
						<div class="well">
							<span class="result">result:</span>
							<p>Have you ever heard of <a href="http://www.google.com" target="_blank">Google?</a></p>
						</div>
						<h5>External Images</h5>
						<p>![enter image description here](http://google.com/img.jpg "enter image title here")</p>
						<p><strong>Note:</strong> Once you save your document, your link and image addresses may appear listed at the bottom, with references throughout the document. This will render the same way.</p>
					</div>
